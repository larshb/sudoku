def sudoku_board(data : List) -> str:
    N2 = len(data)
    N = round(N2 ** 0.5)
    assert N*N == N2, "Data must be k^2 x k^2 for an integer k"

    def divider_row(main='━', left='┠', right='┨', minor='┿', major='╋') -> str:
        s = ""
        for c0 in range(N):
            if c0 == 0:
                s += left
            else:
                s += major
            for c1 in range(N):
                s += main*3
                if c1 != N-1:
                    s += minor
        s += right + "\n"
        return s

    s = ""
    for r0 in range(N):
        if r0 == 0:
            s += divider_row(main='━', left='┏', right='┓', minor='┯', major='┳')
        else:
            s += divider_row(main='━', left='┠', right='┨', minor='┿', major='╋')
        for r1 in range(N):
            if r1 != 0:
                s += divider_row(main='─', left='┠', right='┨', minor='┼', major='╂')
            for c0 in range(N):
                s += "┃"
                for c1 in range(N):
                    r, c = r0*N+r1, c0*N+c1
                    v = data[r][c]
                    if c1 != 0:
                        s += "│"
                    if v == 0:
                        s+= "   "
                    elif isinstance(v, int):
                        s += f"{v:2d} "
                    else:
                        s += "xxx"
            s += "┃\n"
    s += divider_row(main='━', left='┗', right='┛', minor='┷', major='┻')

    return s

if __name__ == "__main__":
    N = 4
    data = [[0]*N*N for _ in range(N*N)]
    print(sudoku_board(data))
